public class SwitchClass {
    public static void main(String[] args) {
        int i = 1;
        switch(i){
            case 1:
                System.out.println("This is example of Switch Case Loop. Case No. " +i);
                break;
            case 2:
                System.out.println("This is example of Switch Case Loop. Case No. " +i);
                break;
            default:
                System.out.println("This is default msg");
        }
    }
}
